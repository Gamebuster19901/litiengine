package de.gurkenlabs.litiengine.configuration;

/**
 * Enum representing the different display modes available.
 */
public enum DisplayMode {
  /**
   * Fullscreen display mode.
   */
  FULLSCREEN,

  /**
   * Windowed display mode.
   */
  WINDOWED,

  /**
   * Borderless window display mode.
   */
  BORDERLESS,
}
